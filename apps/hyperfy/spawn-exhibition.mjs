#!/usr/bin/env node
/**
 * Spawn a MOCA exhibition into a Hyperfy world.
 *
 * Takes the `*.moca-exhibition.json` file exported from the museum's world
 * builder (museumofcryptoart.com/exhibitions/world → Export) and recreates it
 * inside a running Hyperfy world: every room GLB is uploaded as an app, and a
 * generated per-room script hangs the curated artworks onto the same
 * `Slot_NNN` placeholder nodes the museum uses — true aspect ratios, curator
 * move/resize overrides, and live video textures included.
 *
 * Privacy by design: the exhibition file lives on the curator's device. This
 * script is the explicit "upload it into Hyperfy" moment — nothing reaches a
 * world until the curator runs it.
 *
 * Usage:
 *   node spawn-exhibition.mjs my-show.moca-exhibition.json \
 *     --hyperfy /path/to/hyperfy            # cloned repo, see below
 *     --url http://localhost:3000           # world server (ws+api derived)
 *     [--admin-code CODE]                   # if the world sets ADMIN_CODE
 *     [--unit-scale 1]                      # museum-tile → meter multiplier
 *     [--art-size 2]                        # base artwork size in meters
 *     [--name "Exhibition Bot"]
 *
 * One-time Hyperfy setup (the integration drives their official node client):
 *   git clone https://github.com/hyperfy-xyz/hyperfy && cd hyperfy
 *   npm install && npm run node-client:build
 */

import crypto from "node:crypto";
import { readFileSync } from "node:fs";
import path from "node:path";

// ---------------------------------------------------------------- args ----
const args = process.argv.slice(2);
const file = args.find((a) => !a.startsWith("--"));
const opt = (name, fallback) => {
  const i = args.indexOf(`--${name}`);
  return i >= 0 ? args[i + 1] : fallback;
};

if (!file) {
  console.error("Usage: node spawn-exhibition.mjs <exhibition.json> --hyperfy <dir> --url <http://world:3000>");
  process.exit(1);
}

const HYPERFY_DIR = opt("hyperfy", process.env.HYPERFY_DIR);
const BASE_URL = (opt("url", process.env.HYPERFY_WORLD_URL || "http://localhost:3000")).replace(/\/$/, "");
const ADMIN_CODE = opt("admin-code", process.env.HYPERFY_ADMIN_CODE || "");
const UNIT_SCALE = Number(opt("unit-scale", "1"));
const ART_SIZE = Number(opt("art-size", "2"));
const BOT_NAME = opt("name", "MOCA Exhibition Bot");

const WS_URL = BASE_URL.replace(/^http/, "ws") + "/ws";
const API_URL = BASE_URL + "/api";

if (!HYPERFY_DIR) {
  console.error("Missing --hyperfy <dir> (a hyperfy clone with `npm run node-client:build` done).");
  process.exit(1);
}

const exhibition = JSON.parse(readFileSync(file, "utf8"));
if (exhibition.format !== "moca-exhibition@1") {
  console.error(`Unsupported format: ${exhibition.format}`);
  process.exit(1);
}

// ------------------------------------------------------------- helpers ----
const sha256 = (buf) => crypto.createHash("sha256").update(buf).digest("hex");

async function uploadAsset(buffer, ext, mime) {
  const filename = `${sha256(buffer)}.${ext}`;
  const check = await fetch(`${API_URL}/upload-check?filename=${filename}`).then((r) => r.json());
  if (!check.exists) {
    const form = new FormData();
    form.append("file", new File([buffer], filename, { type: mime }));
    const res = await fetch(`${API_URL}/upload`, { method: "POST", body: form });
    if (!res.ok) throw new Error(`Upload failed (${res.status}) for ${filename}`);
  }
  return `asset://${filename}`;
}

/** Per-room Hyperfy app script with the curation inlined — no props plumbing. */
function roomScript(artworks) {
  const config = artworks.map((a) => ({
    slotId: a.slotId,
    imageUrl: a.imageUrl,
    videoUrl: a.videoUrl,
    ratio: a.ratio || 1,
    override: a.override || null,
    label: [a.name, a.artist].filter(Boolean).join(" — "),
  }));
  return `// Generated by MOCA spawn-exhibition — hangs curated artworks onto
// the room's Slot_NNN placeholder nodes (same convention as the museum).
const ARTWORKS = ${JSON.stringify(config)}
const ART_SIZE = ${ART_SIZE}

for (const art of ARTWORKS) {
  try {
    const ov = art.override || { dx: 0, dy: 0, scale: 1 }
    const ratio = art.ratio > 0 ? art.ratio : 1
    const scale = ov.scale || 1
    const width = (ratio >= 1 ? ART_SIZE : ART_SIZE * ratio) * scale
    const height = (ratio >= 1 ? ART_SIZE / ratio : ART_SIZE) * scale

    let node = null
    if (art.videoUrl) {
      node = app.create('video', {
        src: art.videoUrl,
        width,
        height,
        loop: true,
        volume: 0.5,
        spatial: true,
        doubleside: true,
      })
    } else if (art.imageUrl) {
      node = app.create('image', {
        src: art.imageUrl,
        width,
        height,
        fit: 'contain',
        lit: false,
        doubleside: true,
      })
    }
    if (!node) continue

    const slot = app.get(art.slotId)
    if (slot) {
      // Parent to the slot so the artwork inherits the authored transform,
      // then nudge off the wall + apply the curator's move override.
      try { slot.visible = false } catch (e) {}
      try {
        slot.add(node)
        node.position.set((ov.dx || 0), (ov.dy || 0), 0.05)
      } catch (e) {
        // Some GLB nodes can't parent — fall back to the slot's local spot.
        app.add(node)
        node.position.set(slot.position.x + (ov.dx || 0), slot.position.y + (ov.dy || 0), slot.position.z)
        node.quaternion.copy(slot.quaternion)
      }
    } else {
      app.add(node)
    }

    if (art.videoUrl && world.isClient) {
      try { node.play() } catch (e) {}
    }
  } catch (e) {
    console.error('artwork failed', art.slotId, e?.message)
  }
}
`;
}

const yawToQuaternion = (y) => [0, Math.sin(y / 2), 0, Math.cos(y / 2)];

// ---------------------------------------------------------------- main ----
const { createNodeClientWorld } = await import(
  path.join(path.resolve(HYPERFY_DIR), "build", "world-node-client.js")
);

const world = createNodeClientWorld();
world.init({ wsUrl: WS_URL, name: BOT_NAME });

world.once("ready", async () => {
  try {
    console.log(`Connected to ${WS_URL} as "${BOT_NAME}"`);

    if (ADMIN_CODE) {
      world.network.send("command", { args: ["admin", ADMIN_CODE] });
      await new Promise((r) => setTimeout(r, 500));
      console.log("Requested builder rank via admin code.");
    }

    let rooms = 0;
    let artworks = 0;

    for (const placement of exhibition.placements) {
      console.log(`\n→ ${placement.room.title} (${placement.artworks.length} works)`);

      // 1. Room model
      const glbRes = await fetch(placement.room.modelUrl);
      if (!glbRes.ok) {
        console.error(`  ✗ room model unreachable (${glbRes.status}), skipping`);
        continue;
      }
      const glb = Buffer.from(await glbRes.arrayBuffer());
      const modelUrl = await uploadAsset(glb, "glb", "model/gltf-binary");
      console.log(`  model ${modelUrl} (${(glb.length / 1e6).toFixed(1)} MB)`);

      // 2. Per-room curation script
      const script = Buffer.from(roomScript(placement.artworks), "utf8");
      const scriptUrl = await uploadAsset(script, "js", "text/javascript");

      // 3. Blueprint + pinned entity
      const blueprintId = crypto.randomUUID();
      world.network.send("blueprintAdded", {
        id: blueprintId,
        version: 0,
        name: `MOCA · ${placement.room.title}`,
        model: modelUrl,
        script: scriptUrl,
        props: {},
        preload: false,
        public: false,
        locked: false,
        frozen: false,
        unique: false,
        disabled: false,
      });

      world.network.send("entityAdded", {
        id: crypto.randomUUID(),
        type: "app",
        blueprint: blueprintId,
        position: [
          placement.position[0] * UNIT_SCALE,
          placement.position[1] || 0,
          placement.position[2] * UNIT_SCALE,
        ],
        quaternion: yawToQuaternion(placement.rotationY || 0),
        scale: [1, 1, 1],
        mover: null,
        uploader: null,
        pinned: true,
        state: {},
      });

      rooms++;
      artworks += placement.artworks.length;
      await new Promise((r) => setTimeout(r, 300));
    }

    console.log(`\n✔ Spawned ${rooms} room(s), ${artworks} artwork(s) into ${BASE_URL}`);
    console.log("Walk in and enjoy. (Entities are pinned; unpin in-world to rearrange.)");
    setTimeout(() => {
      world.destroy();
      process.exit(0);
    }, 1500);
  } catch (err) {
    console.error("Spawn failed:", err);
    world.destroy();
    process.exit(1);
  }
});

world.on("kick", (reason) => {
  console.error("Kicked from world:", reason, "— is ADMIN_CODE required/correct?");
  process.exit(1);
});
