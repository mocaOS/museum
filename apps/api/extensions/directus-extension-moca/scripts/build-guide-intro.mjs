#!/usr/bin/env node
/**
 * Aggregate the MOCA "how exhibitions work" briefing the in-world museum guide
 * reasons over on its FAST (non-Cortex) path.
 *
 * The guide's first reply has no Cortex retrieval behind it — just the world's
 * room/artwork metadata plus this aggregated brief. Rather than hand-write it,
 * we concatenate the ground-truth docs already maintained in apps/docs (the
 * same source behind docs.museumofcryptoart.com/llms-full.txt) into one compact
 * markdown brief, then emit it as a committed TS string the extension imports
 * (so it bundles into dist/api.js — no runtime fs / network).
 *
 * Run: npm run build:intro   (from the extension dir)
 * Emits: src/v1/guide-intro.md (human-readable) + src/v1/guide-intro.generated.ts
 *
 * Walk/stripping modelled on apps/hyperfy/harvest-hyperfy-docs.mjs.
 */

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const DOCS = path.resolve(scriptDir, "../../../../docs");
const OUT_DIR = path.resolve(scriptDir, "../src/v1");

// Curated source set, in the order it reads best for an exhibition guide.
// Each entry becomes a section in the brief.
// Ordered most-relevant-first for an in-world guide: the guide context clamps
// this brief, so the exhibition/world material must lead (API-integrator
// background trails and is the first to be trimmed).
const SOURCES = [
  { file: "pages/introduction.mdx", title: "What the Museum of Crypto Art is" },
  { file: "pages/hyperfy.mdx", title: "How exhibitions become walkable worlds" },
  { file: "public/skills/exhibitions/SKILL.md", title: "Exhibitions, rooms & the slot convention" },
  { file: "public/skills/museum-guide/SKILL.md", title: "How the museum guide answers visitors" },
  { file: "pages/architecture.mdx", title: "How the museum's data & 3D rooms are built" },
];

/** Strip MDX/MD frontmatter, import lines, and JSX component tags (keep text). */
function clean(md) {
  let s = md;
  // Leading --- frontmatter --- block.
  s = s.replace(/^﻿?---\n[\s\S]*?\n---\n/, "");
  // `import X from "..."` / `export const X = ...` MDX preamble lines.
  s = s.replace(/^\s*(?:import|export)\s.*$/gm, "");
  // JSX component tags (capitalized) like <Callout ...> … </Callout> — keep inner text.
  s = s.replace(/<\/?[A-Z][A-Za-z0-9]*(?:\s[^>]*)?\/?>/g, "");
  // Collapse 3+ blank lines.
  s = s.replace(/\n{3,}/g, "\n\n");
  return s.trim();
}

const parts = [
  "# How the Museum of Crypto Art and its exhibitions work",
  "",
  "This is your background briefing as the museum's in-world guide. It explains " +
    "what MOCA is, how its 3D exhibitions are built and spawned into walkable " +
    "worlds, and how rooms and artworks are organized. Use it to orient visitors " +
    "and connect what they see to the wider museum. Treat the live exhibition " +
    "facts you are given as authoritative over anything here.",
];

let used = 0;
for (const src of SOURCES) {
  const abs = path.join(DOCS, src.file);
  if (!existsSync(abs)) {
    console.warn(`  skip (missing): ${src.file}`);
    continue;
  }
  const body = clean(readFileSync(abs, "utf8"));
  if (!body) continue;
  parts.push("", `## ${src.title}`, "", body);
  used++;
}

const intro = parts.join("\n").replace(/\n{3,}/g, "\n\n").trim() + "\n";

if (!used) {
  console.error("No source docs found under apps/docs — nothing aggregated.");
  process.exit(1);
}

writeFileSync(path.join(OUT_DIR, "guide-intro.md"), intro);

const generated = `/**
 * GENERATED by scripts/build-guide-intro.mjs — do not edit by hand.
 * Aggregated MOCA exhibition briefing baked into the museum guide's fast-path
 * context (see guide.ts / museum-agent.ts). Regenerate with: npm run build:intro
 */

export const MOCA_GUIDE_INTRO = ${JSON.stringify(intro)};
`;
writeFileSync(path.join(OUT_DIR, "guide-intro.generated.ts"), generated);

console.log(
  `✔ Aggregated ${used}/${SOURCES.length} docs → guide-intro.md + guide-intro.generated.ts (${(intro.length / 1024).toFixed(1)} KB)`,
);
